void lcd_clear_screen(void) {
  long i;
	
	uart_put_str("lcd_clear_screen");
	
  // Row address set  (command 0x2B)
  write_spi_command(PASET);
  write_spi_data(0);
  write_spi_data(131);
  // loop counter
  // Column address set  (command 0x2A)
  write_spi_command(CASET);
  write_spi_data(0);
  write_spi_data(131);
  // set the display memory to BLACK
  write_spi_command(RAMWR);
  for(i = 0; i < ((131 * 131) / 2); i++) {
    write_spi_data((BLACK >> 4) & 0xFF);
    write_spi_data(((BLACK & 0xF) << 4) | ((BLACK >> 8) & 0xF));
    write_spi_data(BLACK & 0xFF);
  }
}

void lcd_set_pixel(int x, int y, int color) {
	
  // Row address set  (command 0x2B)
  write_spi_command(PASET);
  write_spi_data(x);
  write_spi_data(x);
  // Column address set  (command 0x2A)
  write_spi_command(CASET);
  write_spi_data(y);
  write_spi_data(y);
  // Now illuminate the pixel (2nd pixel will be ignored)
  write_spi_command(RAMWR);
  write_spi_data((color >> 4) & 0xFF);
  write_spi_data(((color & 0xF) << 4) | ((color >> 8) & 0xF));
  write_spi_data(color & 0xFF);
}

void lcd_set_line(int x0, int y0, int x1, int y1, int color) {
  int dy = y1 - y0;
  int dx = x1 - x0;
  int stepx, stepy;

  if(dy < 0){
    dy=-dy;
    stepy=-1;
  }else{
    stepy=1;
  }
  if( dx < 0 ){
    dx=-dx;
    stepx=-1;
  }else{
    stepx=1;
  }
  dy <<= 1; // dy is now 2*dy
  dx <<= 1; // dx is now 2*dx

  lcd_set_pixel(x0, y0, color);
  if (dx > dy) {
    int fraction = dy - (dx >> 1);// same as 2*dy - dx
    while (x0 != x1) {
      if (fraction >= 0) {
        y0 += stepy;
        fraction -= dx; // same as fraction -= 2*dx
      }
      x0 += stepx;
      fraction += dy; // same as fraction -= 2*dy
      lcd_set_pixel(x0, y0, color);
    }
  }else{
    int fraction = dx - (dy >> 1);
    while (y0 != y1) {
      if (fraction >= 0) {
        x0 += stepx;
        fraction -= dy;
      }
      y0 += stepy;
      fraction += dx;
      lcd_set_pixel(x0, y0, color);
    }
  }

}

void lcd_set_rect(int x0, int y0, int x1, int y1, unsigned char fill, int color) {
  int xmin, xmax, ymin, ymax;
  int i;
  // check if the rectangle is to be filled
  if (fill == FILL) {
    // best way to create a filled rectangle is to define a drawing box
    // and loop two pixels at a time
    // calculate the min and max for x and y directions
    xmin = (x0 <= x1) ? x0 : x1;
    xmax = (x0 > x1) ? x0 : x1;
    ymin = (y0 <= y1) ? y0 : y1;
    ymax = (y0 > y1) ? y0 : y1;
    // specify the controller drawing box according to those limits
    // Row address set (command 0x2B)
    write_spi_command(PASET);
    write_spi_data(xmin);
    write_spi_data(xmax);
    // Column address set  (command 0x2A)
    write_spi_command(CASET);
    write_spi_data(ymin);
    write_spi_data(ymax);
    // WRITE MEMORY
    write_spi_command(RAMWR);
    // loop on total number of pixels / 2
    for (i = 0; i < ((((xmax - xmin + 1) * (ymax - ymin + 1)) / 2) + 130); i++) {
      // use the color value to output three data bytes covering two pixels
      write_spi_data((color >> 4) & 0xFF);
      write_spi_data(((color & 0xF) << 4) | ((color >> 8) & 0xF));
      write_spi_data(color & 0xFF);
    }
  } else {
    // best way to draw un unfilled rectangle is to draw four lines
    lcd_set_line(x0, y0, x1, y0, color);
    lcd_set_line(x0, y1, x1, y1, color);
    lcd_set_line(x0, y0, x0, y1, color);
    lcd_set_line(x1, y0, x1, y1, color);
  }
}

void lcd_set_circle(int x0, int y0, int radius, int color) {
  int f = 1 - radius;
  int ddF_x = 0;
  int ddF_y = -2 * radius;
  int x = 0;
  int y = radius;
  lcd_set_pixel(x0, y0 + radius, color);
  lcd_set_pixel(x0, y0 - radius, color);
  lcd_set_pixel(x0 + radius, y0, color);
  lcd_set_pixel(x0 - radius, y0, color);

  while(x < y) {
    if(f >= 0) {
      y--;
      ddF_y += 2;
      f += ddF_y;
    }
    x++;
    ddF_x += 2;
    f += ddF_x + 1;
    lcd_set_pixel(x0 + x, y0 + y, color);
    lcd_set_pixel(x0 - x, y0 + y, color);
    lcd_set_pixel(x0 + x, y0 - y, color);
    lcd_set_pixel(x0 - x, y0 - y, color);
    lcd_set_pixel(x0 + y, y0 + x, color);
    lcd_set_pixel(x0 - y, y0 + x, color);
    lcd_set_pixel(x0 + y, y0 - x, color);
    lcd_set_pixel(x0 - y, y0 - x, color);
  }
}

void lcd_put_char(char c, int x, int y, int size, int fcolor, int bcolor) {
  /*extern const unsigned char FONT6x8[97][8];
  extern const unsigned char FONT8x8[97][8];
  extern const unsigned char FONT8x16[97][16];*/
  int i,j;
  unsigned int nCols;
  unsigned int nRows;
  unsigned int nBytes;
  unsigned char PixelRow;
  unsigned char Mask;

  unsigned int Word0;
  unsigned int Word1;
  unsigned char *pFont;
  unsigned char *pChar;
  unsigned char *FontTable[] = {(unsigned char *)FONT6x8, (unsigned char *)FONT8x8, (unsigned char *)FONT8x16};

  // get pointer to the beginning of the selected font table
  pFont = (unsigned char *)FontTable[size];
  // get the nColumns, nRows and nBytes
  nCols = *pFont;
  nRows = *(pFont + 1);
  nBytes = *(pFont + 2);
  // get pointer to the last byte of the desired character
  pChar = pFont + (nBytes * (c - 0x1F)) + nBytes - 1;
  // Row address set  (command 0x2B)
  write_spi_command(PASET);
  write_spi_data(x);
  write_spi_data(x + nRows - 1);
  // Column address set  (command 0x2A)
  write_spi_command(CASET);
  write_spi_data(y);
  write_spi_data(y + nCols - 1);

  // Write Memory
  write_spi_command(RAMWR);

  // loop on each row, working backwards from the bottom to the top for (i = nRows - 1; i >= 0; i--) {
  Mask = 0x80;
  for (i = nRows - 1; i >= 0; i--) {
    // copy pixel row from font table and then decrement row
    PixelRow = *pChar--;
    // loop on each pixel in the row (left to right)
    // Note: we do two pixels each loop
    Mask = 0x80;
    for (j = 0; j < nCols; j += 2) {
      // if pixel bit set, use foreground color; else use the background color
      // now get the pixel color for two successive pixels
      if((PixelRow & Mask) == 0) {
        Word0 = bcolor;
      } else {
        Word0 = fcolor;
      }
      Mask = Mask >> 1;
      if ((PixelRow & Mask) == 0) {
        Word1 = bcolor;
      } else {
        Word1 = fcolor;
      }
      Mask = Mask >> 1;
      // use this information to output three data bytes
      write_spi_data((Word0 >> 4) & 0xFF);
      write_spi_data(((Word0 & 0xF) << 4) | ((Word1 >> 8) & 0xF));
      write_spi_data(Word1 & 0xFF);
    }
  }
}

void lcd_put_str(char* string, int x, int y, int size, int fcolor, int bcolor) {
  // loop until null-terminator is seen
  while (*string != 0x00) {
    // draw the character
    lcd_put_char(*string++, x, y, size, fcolor, bcolor);
    // advance the y position
    if (size == SMALL) {
      y = y + 6;
    } else if (size == MEDIUM) {
      y = y + 8;
    } else {
      y = y + 8;
    }
    // bail out if y exceeds 131
    if (y > 131) break;
  }
}

